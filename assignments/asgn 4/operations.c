#include <stdio.h>

int addnums(int first, int second) {
    return first + second;
}
int subnums(int first, int second) {
    return first - second;
}
int multnums(int first, int second) {
    return first * second;
}
int divnums(int first, int second) {
    return first / second;
}
int andnums(int first, int second) {
    return first & second;
}
int ornums(int first, int second) {
    return first | second;
}
int xornums(int first, int second) {
    return first ^ second;
}
int lshiftnums(int first, int second) {
    return first << second;
}
unsigned int rshiftnums(unsigned int first, int second) {
    return first >> second;
}